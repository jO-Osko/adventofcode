def next_iteration(board, broken_lights=False):
    copy = [[k for k in line] for line in board]
    for x in range(len(board)):
        for y in range(len(board[x])):
            su = -copy[x][y]
            for dx in [-1,0,1]:
                for dy in [-1,0,1]:
                    if x+dx >= 0 and y+dy >= 0 and x+dx < len(board) and y+dy < len(board[x]):
                        su += copy[x+dx][y+dy]
            if copy[x][y]:
                if su in (2,3):
                    board[x][y] = 1
                else:
                    board[x][y] = 0
            else:
                if su == 3:
                    board[x][y] = 1
    if broken_lights:
        board[0][0] = board[0][-1] = board[-1][0] = board[-1][-1] = 1
    return board

def part1(data, num=100):
    board = [[int(k=="#") for k in line] for line in data.split("\n")]
    for j in range(num):
        board = next_iteration(board)

    return sum(map(sum, board))

def part2(data, num=100):
    board = [[int(k=="#") for k in line] for line in data.split("\n")]
    board[0][0] = board[0][-1] = board[-1][0] = board[-1][-1] = 1
    for j in range(num):
        board = next_iteration(board, True)
    return sum(map(sum, board))


